import math
import random

class StructuralBiophysicistAgent:
    def __init__(self):
        self.name = "Structural Biophysicist Agent"
        # Standard three-letter code mapping
        self.aa_mapping = {
            'A': 'ALA', 'R': 'ARG', 'N': 'ASN', 'D': 'ASP', 'C': 'CYS',
            'Q': 'GLN', 'E': 'GLU', 'G': 'GLY', 'H': 'HIS', 'I': 'ILE',
            'L': 'LEU', 'K': 'LYS', 'M': 'MET', 'F': 'PHE', 'P': 'PRO',
            'S': 'SER', 'T': 'THR', 'W': 'TRP', 'Y': 'TYR', 'V': 'VAL'
        }

    def generate_helical_pdb(self, sequence: str) -> str:
        """
        Generates a valid PDB string representing an alpha-helix structure of the peptide
        surrounded by a simulated lipid membrane bilayer (POPG/POPE headgroups).
        """
        pdb_lines = []
        atom_serial = 1
        
        # Alpha-helix parameters
        R_ca = 2.3  # Radius of CA helix
        d_ca = 1.5  # Rise per residue along Z axis
        phi_per_res = math.radians(100.0)  # 100 degrees rotation per residue

        # Center the peptide in the box
        # Total length in Z is (N-1) * 1.5. Center it at Z = 0
        n_res = len(sequence)
        z_offset = -((n_res - 1) * d_ca) / 2.0

        pdb_lines.append(f"HEADER    DE NOVO AMP ALPHA-HELIX STRUCTURE - GENERATED BY CRYPTAM")
        pdb_lines.append(f"TITLE     PEPTIDE: {sequence}")
        
        for idx, aa in enumerate(sequence):
            res_num = idx + 1
            res_name = self.aa_mapping.get(aa.upper(), 'ALA')
            
            # Calculate angle for this residue
            theta = idx * phi_per_res
            
            # Backbone atom positions (rough parametric approximations for an alpha-helix)
            # CA (Alpha Carbon)
            ca_x = R_ca * math.cos(theta)
            ca_y = R_ca * math.sin(theta)
            ca_z = idx * d_ca + z_offset
            
            # N (Nitrogen) - slightly rotated back and down
            n_theta = theta - 0.7
            n_x = 2.1 * math.cos(n_theta)
            n_y = 2.1 * math.sin(n_theta)
            n_z = idx * d_ca - 0.5 + z_offset
            
            # C (Carbonyl Carbon) - slightly rotated forward and up
            c_theta = theta + 0.7
            c_x = 2.1 * math.cos(c_theta)
            c_y = 2.1 * math.sin(c_theta)
            c_z = idx * d_ca + 0.5 + z_offset
            
            # O (Carbonyl Oxygen) - further out from C
            o_theta = theta + 1.2
            o_x = 3.0 * math.cos(o_theta)
            o_y = 3.0 * math.sin(o_theta)
            o_z = idx * d_ca + 0.8 + z_offset
            
            # Side chain CB (Beta Carbon) - pointing outward radially
            cb_x = 3.3 * math.cos(theta)
            cb_y = 3.3 * math.sin(theta)
            cb_z = idx * d_ca + z_offset

            # Format PDB ATOM lines
            # N
            pdb_lines.append(f"ATOM  {atom_serial:5d}  N   {res_name} A{res_num:4d}    {n_x:8.3f}{n_y:8.3f}{n_z:8.3f}  1.00 20.00           N")
            atom_serial += 1
            # CA
            pdb_lines.append(f"ATOM  {atom_serial:5d}  CA  {res_name} A{res_num:4d}    {ca_x:8.3f}{ca_y:8.3f}{ca_z:8.3f}  1.00 20.00           C")
            atom_serial += 1
            # C
            pdb_lines.append(f"ATOM  {atom_serial:5d}  C   {res_name} A{res_num:4d}    {c_x:8.3f}{c_y:8.3f}{c_z:8.3f}  1.00 20.00           C")
            atom_serial += 1
            # O
            pdb_lines.append(f"ATOM  {atom_serial:5d}  O   {res_name} A{res_num:4d}    {o_x:8.3f}{o_y:8.3f}{o_z:8.3f}  1.00 20.00           O")
            atom_serial += 1
            # CB (unless it's Glycine)
            if res_name != 'GLY':
                pdb_lines.append(f"ATOM  {atom_serial:5d}  CB  {res_name} A{res_num:4d}    {cb_x:8.3f}{cb_y:8.3f}{cb_z:8.3f}  1.00 20.00           C")
                atom_serial += 1
                
        # HELIX record to tell 3Dmol.js it is an alpha-helix explicitly
        pdb_lines.append(f"HELIX    1   1 THR A    1 LEU A {n_res:4d}  1                                  {n_res:4d}")
        
        # Add Simulated Membrane Bilayer (POPG / POPE Lipid headgroups)
        # We will represent headgroups using Phosphorus (P) and Nitrogen (N) atoms
        # Placed as grids in two leaflets: Upper at Z = 12.0, Lower at Z = -12.0
        pdb_lines.append("REMARK    SIMULATED MEMBRANE BILAYER (POPE/POPG HEADGROUPS)")
        
        lipid_res_num = n_res + 2
        for z_val, leaflet in [(12.0, "UP"), (-12.0, "LO")]:
            for x_idx in range(-3, 4):
                for y_idx in range(-3, 4):
                    # Introduce a slight noise to make it look realistic (fluid membrane)
                    x = x_idx * 7.0 + random.uniform(-1.0, 1.0)
                    y = y_idx * 7.0 + random.uniform(-1.0, 1.0)
                    z = z_val + random.uniform(-0.5, 0.5)
                    
                    # Alternate between POPE (Phosphatidylethanolamine) and POPG (Phosphatidylglycerol)
                    is_pe = (x_idx + y_idx) % 2 == 0
                    lipid_name = "POPE" if is_pe else "POPG"
                    
                    # Phosphorus atom
                    pdb_lines.append(f"ATOM  {atom_serial:5d}  P   {lipid_name} B{lipid_res_num:4d}    {x:8.3f}{y:8.3f}{z:8.3f}  1.00 40.00           P")
                    atom_serial += 1
                    
                    # Choline/Ethanolamine Nitrogen or Glycerol Oxygen (offset by 2A in Z away from center)
                    offset_z = z + (2.0 if z_val > 0 else -2.0)
                    atom_name = "N" if is_pe else "O"
                    pdb_lines.append(f"ATOM  {atom_serial:5d}  {atom_name}   {lipid_name} B{lipid_res_num:4d}    {x:8.3f}{y:8.3f}{offset_z:8.3f}  1.00 40.00           {atom_name}")
                    atom_serial += 1
                    
                    lipid_res_num += 1
                    
        pdb_lines.append("END")
        return "\n".join(pdb_lines)

