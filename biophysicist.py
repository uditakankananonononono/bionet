    def run_docking_simulation(self, peptide_seq: str, target: str):
        """Simulates HDOCK/HADDOCK docking against antibiotic resistance targets."""
        logs = []
        logs.append(f"[Biophysicist] Initializing macromolecular docking of peptide against target: {target}")
        
        # Determine affinity parameters depending on sequence charge and hydrophobic density
        charge = sum(1 for aa in peptide_seq if aa in ['K', 'R']) - sum(1 for aa in peptide_seq if aa in ['D', 'E'])
        
        # Docking scoring heuristics
        base_energy = -180.0 # Standard HDOCK docking score
        
        if target == "Lipid-II Precursor (Cell Wall)":
            # Lipid-II is highly negative (pyrophosphate group), binds highly cationic peptides (e.g. vancomycin, nisin)
            affinity = -180.0 - (charge * 18.5) + random.uniform(-10.0, 10.0)
            k_d = 10 ** (affinity / 30.0) * 1000.0 # nM range
            interface_residues = "PP-group, MurNAc, D-Ala3, D-Ala4"
        elif target == "TEM-1 Beta-Lactamase (Resonance Enzyme)":
            # Protein target, requires steric fit and key hydrogen bonds
            affinity = -210.0 - (len(peptide_seq) * 2.1) + random.uniform(-15.0, 15.0)
            k_d = 10 ** (affinity / 35.0) * 1000.0
            interface_residues = "Glu166, Ser70, Lys73, Ala237"
        else:
            affinity = -150.0 + random.uniform(-20.0, 20.0)
            k_d = 5000.0
            interface_residues = "Non-specific binding"
            
        logs.append(f"[Biophysicist] HDOCK grid search computed 10,000 orientations.")
        logs.append(f"[Biophysicist] Best docking pose energy: {affinity:.2f} kcal/mol (Estimated Kd: {k_d:.2f} nM)")
        
        return {
            "logs": logs,
            "docking_score": round(affinity, 2),
            "est_kd_nm": round(k_d, 2),
            "interface_residues": interface_residues,
            "hydrogen_bonds": random.randint(3, 8)
        }

    def run_membrane_simulation(self, peptide_seq: str):
        """Simulates Martini GROMACS membrane insertion statistics."""
        logs = []
        logs.append(f"[Biophysicist] Setting up coarse-grained Martini systems...")
        logs.append(f"[Biophysicist] Built POPE:POPG (3:1) bacterial inner membrane bilayer patch (10x10nm).")
        logs.append(f"[Biophysicist] Placed peptide in solvent. Solvated with Martini water beads + 150mM NaCl.")
        logs.append(f"[Biophysicist] Energy minimized with steepest descent. Max force < 10.0 kJ/mol/nm.")
        
        # Calculate membrane insertion metrics based on hydrophobicity and charge
        # Highly hydrophobic/amphipathic inserts well.
        h_moment = sum(1 for aa in peptide_seq if aa in ['L', 'I', 'V', 'F', 'W']) / len(peptide_seq)
        
        # Biophysical metrics
        free_energy_ins = -15.0 - (h_moment * 40.0) + random.uniform(-3.0, 3.0) # Delta G of insertion in kcal/mol
        scd_order = 0.35 - (h_moment * 0.15) # Lipid order parameter (lower means more disruption/packing leakage)
        lipid_leakage = round(h_moment * 85.0 + random.uniform(0, 10), 1) # % leakage of dye/ions
        
        logs.append(f"[Biophysicist] MD simulation running for 100 ns (5,000,000 steps)...")
        logs.append(f"[Biophysicist] Peptide partition reached equilibrium at t = 35 ns.")
        logs.append(f"[Biophysicist] Free Energy of Insertion (ΔG_ins): {free_energy_ins:.2f} kcal/mol.")
        logs.append(f"[Biophysicist] Lipid packing order disruption (S_CD) observed. Packing index: {scd_order:.3f}")
        
        return {
            "logs": logs,
            "free_energy_insertion_kcal_mol": round(free_energy_ins, 2),
            "lipid_order_disruption_scd": round(scd_order, 3),
            "membrane_leakage_pct": lipid_leakage,
            "structure_pdb": self.generate_helical_pdb(peptide_seq)
        }
